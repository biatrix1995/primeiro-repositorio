/*Continuação dos estudos de Arrays*/

const lista = ['João', 'Maria', 'Paulo', 'Pedro', 'Vitoria', 'Augusto', 'Fabio', 'Larissa']
console.log(lista)
lista.push('Roberta') //adciona um valor no final do array
console.log(lista)
lista.unshift('Gustavo') //adiciona um valor no início do array
console.log(lista)
lista.pop//remove o último valor do array
console.log(lista)
lista.shift//remove o primeiro valor do array
console.log(lista)
console.log(lista.concat('Julio')) //cria um novo array adicionando o valor passado como parâmetro
console.log(lista.join('->')) // pega os valores e gera uma string
